function ring(lat: number, lng: number) {
  return [
    { lat: lat + 0.012, lng: lng - 0.018 },
    { lat: lat + 0.012, lng: lng + 0.018 },
    { lat: lat - 0.012, lng: lng + 0.018 },
    { lat: lat - 0.012, lng: lng - 0.018 },
  ];
}

